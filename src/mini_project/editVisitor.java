    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(editFlag==1)
        {
            try
            {
            String query="update visitor set  host_id=?, name=?, country=? where visitor_id='"+search.getText().trim()+"'";
            PreparedStatement stmt;
            Class.forName("oracle.jdbc.driver.OracleDriver");
            Connection con = DriverManager.getConnection(url, userName, password);
            stmt=con.prepareStatement(query);
            stmt.setString(1, hid.getText().trim());
            stmt.setString(2, name.getText().trim());
            stmt.setString(3, country.getText().trim());
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Updated");
            con.close();
            }
            catch (Exception e)
            {
                JOptionPane.showMessageDialog(null, e);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "please select visitor using search");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

